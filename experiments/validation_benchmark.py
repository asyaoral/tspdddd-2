"""Fast validation benchmark for the TSP-D project.

This script supports the project requirements for:
- benchmark testing,
- validation and refinement,
- runtime / robustness analysis,
- comparison of the proposed HGA-SHC method against baseline methods.

It uses fixed random seeds so the results are reproducible.
"""

from __future__ import annotations

import csv
import random
import statistics as stats
import time
from pathlib import Path

from tspd.tspd_utils import generate_nodes, compute_distance_matrix
from solvers.hga_solver import hga_solve
from solvers.grasp_solver import grasp_solve
from solvers.hga_shc import hga_shc_solve


# Small enough to finish quickly, but enough to show scalability.
INSTANCE_SIZES = [5, 10, 20]
SEEDS = [1, 2, 3]

# Fast parameters for last-minute validation. Full benchmark can use larger values.
ITERATIONS = {
    "HGA": 30,
    "GRASP": 30,
    "HGA-SHC": 30,
}
POP_SIZE = 12


def run_solver(name: str, nodes, dist_matrix) -> float:
    if name == "HGA":
        return hga_solve(nodes, dist_matrix, iterations=ITERATIONS[name], pop_size=POP_SIZE)
    if name == "GRASP":
        return grasp_solve(nodes, dist_matrix, iterations=ITERATIONS[name])
    if name == "HGA-SHC":
        return hga_shc_solve(nodes, dist_matrix, iterations=ITERATIONS[name], pop_size=POP_SIZE)
    raise ValueError(f"Unknown solver: {name}")


def percentage_improvement(baseline: float, proposed: float) -> float:
    if baseline == 0:
        return 0.0
    return ((baseline - proposed) / baseline) * 100.0


def main() -> None:
    out_dir = Path("data")
    out_dir.mkdir(exist_ok=True)
    csv_path = out_dir / "validation_results.csv"
    summary_path = out_dir / "validation_summary.md"

    raw_rows = []
    summary_rows = []

    for size in INSTANCE_SIZES:
        per_algorithm = {"HGA": [], "GRASP": [], "HGA-SHC": []}
        per_runtime = {"HGA": [], "GRASP": [], "HGA-SHC": []}

        for seed in SEEDS:
            nodes = generate_nodes(size, seed=seed)
            dist_matrix = compute_distance_matrix(nodes)

            for algorithm in ["HGA", "GRASP", "HGA-SHC"]:
                # Reset the random stream before every solver call to improve reproducibility.
                random.seed(seed)
                start = time.perf_counter()
                cost = run_solver(algorithm, nodes, dist_matrix)
                runtime = time.perf_counter() - start

                per_algorithm[algorithm].append(cost)
                per_runtime[algorithm].append(runtime)
                raw_rows.append({
                    "customers": size,
                    "seed": seed,
                    "algorithm": algorithm,
                    "cost": round(cost, 4),
                    "runtime_seconds": round(runtime, 4),
                })

        hga_avg = stats.mean(per_algorithm["HGA"])
        proposed_avg = stats.mean(per_algorithm["HGA-SHC"])
        improvement = percentage_improvement(hga_avg, proposed_avg)

        for algorithm in ["HGA", "GRASP", "HGA-SHC"]:
            costs = per_algorithm[algorithm]
            runtimes = per_runtime[algorithm]
            summary_rows.append({
                "customers": size,
                "algorithm": algorithm,
                "best_cost": round(min(costs), 4),
                "avg_cost": round(stats.mean(costs), 4),
                "std_cost": round(stats.stdev(costs), 4) if len(costs) > 1 else 0.0,
                "avg_runtime_seconds": round(stats.mean(runtimes), 4),
                "improvement_vs_hga_percent": round(improvement, 2) if algorithm == "HGA-SHC" else "-",
            })

    with csv_path.open("w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(f, fieldnames=list(raw_rows[0].keys()))
        writer.writeheader()
        writer.writerows(raw_rows)

    with summary_path.open("w", encoding="utf-8") as f:
        f.write("# Validation and Benchmark Summary\n\n")
        f.write("This file was generated by `python -m experiments.validation_benchmark`.\n\n")
        f.write("The benchmark uses generated TSP-D instances with fixed seeds. ")
        f.write("Each algorithm is evaluated on the same instances to compare solution quality, runtime, and robustness.\n\n")
        f.write("| Customers | Algorithm | Best Cost | Avg Cost | Std Cost | Avg Runtime (s) | Improvement vs HGA (%) |\n")
        f.write("|---:|---|---:|---:|---:|---:|---:|\n")
        for row in summary_rows:
            f.write(
                f"| {row['customers']} | {row['algorithm']} | {row['best_cost']} | "
                f"{row['avg_cost']} | {row['std_cost']} | {row['avg_runtime_seconds']} | "
                f"{row['improvement_vs_hga_percent']} |\n"
            )
        f.write("\n## Interpretation\n\n")
        f.write("HGA-SHC is the proposed hybrid method. It adds stochastic hill climbing as a local improvement phase after the evolutionary search operations. ")
        f.write("Therefore, it may require more runtime than the simpler baselines, but it is expected to improve solution quality by reducing the final route cost. ")
        f.write("The standard deviation column is used as a robustness indicator across different random seeds.\n")

    print(f"Saved raw results to {csv_path}")
    print(f"Saved summary to {summary_path}")


if __name__ == "__main__":
    main()
